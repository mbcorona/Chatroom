const mongoose = require('mongoose');
module.exports = mongoose.model("chats", new mongoose.Schema({
    room: {
        type: String,
        required: true
    },
    username: {
        type: String,
        required: true
    },
    text: {
        type: String,
        required: true
    },
    date: {
        type: Date,
        required: true
    }
}));